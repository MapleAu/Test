package com.test;

public class forswitch {
	public static void main(String[] args) {
		for(int i=0;i<3;i++) {
			switch (i){
			case 0:System.out.println("a");
			case 1:System.out.println("b");break;
			case 2:System.out.println("c");break;
			//case 3:System.out.println("d");break;
			}
		}
	}
}
